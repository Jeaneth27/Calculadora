<?php
class Operaciones
{
    public function suma($a, $b)
    {
        return ($a+$b);
    }
    public function multiplicacion($a, $b)
    {
        return ($a*$b);
    }
    public function Division($a, $b)
    {
        return ($a/$b);
    }
    public function resta($a, $b)
    {
        return ($a-$b);
    }
}
?>
